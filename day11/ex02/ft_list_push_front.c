#include "ft_list.h"

t_list	*ft_create_elem(void *data);

void	ft_list_push_front(t_list **begin_list, void *data)
{
	t_list *now;

	now = ft_create_elem(data);
	now->next = *begin_list;
	*begin_list = now;
}
