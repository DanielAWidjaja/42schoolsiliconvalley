int		ft_is_sort(int *tab, int len, int (*f) (int, int))
{
	int i;

	i = 0;
	while (i < len - 1)
	{
		if (f(tab[i], tab[i + 1]) > 0)
			return (0);
		i++;
	}
	return (1);
}
