#include "bsq.h"

int		**make_grid(int **grid, t_var *pos)
{
	int i;
	int j;

	i = 0;
	while (i < pos->sz)
	{
		j = 0;
		while (j < pos->sz)
		{
			grid[i + pos->r][j + pos->c] = 2;
			j++;
		}
		i++;
	}
	return (grid);
}
