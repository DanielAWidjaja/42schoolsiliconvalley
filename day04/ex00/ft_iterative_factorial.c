int	ft_iterative_factorial(int nb)
{
	int i;
	int ret;

	if (nb < 0 || 13 < nb)
		return (0);
	i = 1;
	ret = 1;
	while (i <= nb)
	{
		ret *= i;
		i++;
	}
	return (ret);
}
